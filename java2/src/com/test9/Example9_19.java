package com.test9;

import java.awt.Color;
import java.awt.FlowLayout;
import java.awt.event.ActionEvent;
import java.awt.event.ActionListener;

import javax.swing.JButton;
import javax.swing.JColorChooser;
import javax.swing.JFrame;


/**
 * 颜色对话框的程序
 * @author lcj
 *
 */
public class Example9_19 {
	public static void main(String[] args) {
		WindowColor co = new WindowColor();
		co.setTitle("颜色对话框");
		co.setBounds(80,90,200,300);
	}
}

class WindowColor extends JFrame implements ActionListener {
	JButton button;
	public WindowColor() {
		button = new JButton("打开颜色对话框");
		button.addActionListener(this);
		setLayout(new FlowLayout());
		add(button);
		setVisible(true);
		setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
	}
	@Override
	public void actionPerformed(ActionEvent e) {
		Color newColor = JColorChooser.showDialog(this, "调色板", getContentPane().getBackground());
		if(newColor != null){
			getContentPane().setBackground(newColor);
		}
		
	}
	
}



	